function COUPnike10() {
  var element = document.getElementById("COUPnike10");

  if (element.classList) {
    element.classList.toggle("hidden");
  } else {
    var classes = element.className.split(" ");
    var i = classes.indexOf("hidden");

    if (i >= 0)
      classes.splice(i, 1);
    else
      classes.push("hidden");
    element.className = classes.join(" ");
  }
}
